public class A14_SwitchCase {
    public static void main(String[] args){

        int num = 2;
        switch(num){
            case 1:
                System.out.print("Samosa");
                break;
            case 2:
                System.out.print("Mango");
                break;
            case 3:
                System.out.print("Dosa");
                break;
            default:
                System.out.print("Nothing");
                break;

        }
    }
}
