public class A58_BitManipulation {


    public static void main(String[] args) {
        // Binary AND
        System.out.println("AND:" + (5 & 6));

        //Binary OR
        System.out.println("OR:" + (5 | 6));

        //Binary XOR
        System.out.println("XOR:" + (5 ^ 6));

        //Binary NOT : 1's Complement
        System.out.println("NOT:" + (~5));

        //Binary Left Shift
        System.out.println("Left Shift:" + (5<<2));

        //Binary Right Shift
        System.out.println("Right Shift:" + (5>>2));
    }
}
